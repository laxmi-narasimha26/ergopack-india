// Comprehensive Product Data Structure
// Extracted from Model Overview PDFs and Technical Datasheets

export interface ProductAccessory {
  id: string;
  name: string;
  description: string;
  imageUrl?: string;
  category: 'included' | 'optional';
  details?: string[];
}

export interface ProductSpecifications {
  line: 'Economy Line' | 'X-pert Line';
  model: string;
  generation?: string;
  tariffNr: string;
  dimensions: {
    length: number;
    width: number;
    height: number;
    unit: 'mm';
  };
  weight: number;
  weightUnit: 'kg';
  palletWidth: {
    min: number;
    max: number;
    unit: 'cm';
  };
  palletHeight: {
    min: number;
    max: number;
    unit: 'cm';
  };
  chainSpeed: string;
  chainLength: string;
  control: string;
  settingsChange: string;
  strapMaterials?: string[];
  strapWidth?: string;
  strapThickness?: string;
  coilDiameters?: string[];
}

export interface BatteryInfo {
  type: string;
  weight: number;
  loadingTime: string;
  voltage: string;
  strappingCycles: number;
  chargingStation?: {
    type: string;
    operatingVoltage: string;
    powerConsumption: string;
    powerSupply: string;
  };
}

export interface ProductCertifications {
  iso12100?: boolean;
  euDeclaration?: boolean;
  agr?: boolean;
}

export interface ComprehensiveProduct {
  id: string;
  name: string;
  tagline: string;
  description: string;
  line: 'economy' | 'xpert';
  specifications: ProductSpecifications;
  battery?: BatteryInfo;
  features: string[];
  materialCompatibility?: string[];
  includedAccessories: ProductAccessory[];
  optionalAccessories: ProductAccessory[];
  certifications: ProductCertifications;
  applications: string[];
  images: {
    hero: string;
    gallery: string[];
    accessories: string[];
    applications: string[];
  };
  price?: number;
  sealingHead?: any;
  useCases?: any[];
  videoLinks?: string[];
  resources?: any;
}

